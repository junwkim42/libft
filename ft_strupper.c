#include "libft.h"

char	*ft_strupper(char *s, int n)
{
	int i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i] && i < n)
	{
		if ('a' <= s[i] && s[i] <= 'z')
			s[i] = s[i] - 32;
		i++;
	}
	return (s);
}
